    % Regole:
    
    % prec(X, Y): X è precedente a Y se il game_id di X è minore di quello di Y.
    prec(X, Y) :-
        partita(X, _, _, _, _, _, _),
        partita(Y, _, _, _, _, _, _),
        atom_number(X, ID_X),
        atom_number(Y, ID_Y),
        ID_X < ID_Y.
    
    % better(X, Y): X è meglio di Y se ACS di X è maggiore di quello di Y.
    better(X, Y) :-
        partita(X, _, _, _, _, ACS_X, _),
        partita(Y, _, _, _, _, ACS_Y, _),
        ACS_X > ACS_Y.
    
    % newbie(X): X è una partita newbie se il suo game_id è inferiore a 50.
    newbie(X) :-
        partita(X, _, _, _, _, _, _),
        atom_number(X, ID_X),
        ID_X < 50.
    
    % high_kdr(X): X ha un KDR maggiore di 1.5.
    high_kdr(X) :-
        partita(X, _, KDR, _, _, _, _),
        KDR > 1.5.
    
    % high_headshot(X): X ha una percentuale di headshot maggiore di 25.
    high_headshot(X) :-
        partita(X, _, _, _, Headshot_Pct, _, _),
        Headshot_Pct > 25.
    
    % high_assists(X): X ha un numero di assist maggiore di 5.
    high_assists(X) :-
        partita(X, _, _, Assists, _, _, _),
        Assists > 5.
    
    % astonishing(X): una partita è straordinaria.
    astonishing(X) :-
        \+ newbie(X),
        (high_kdr(X); high_headshot(X); high_assists(X)),
        findall(Y, (prec(Y, X), better(X, Y)), BetterGames),
        length(BetterGames, CountBetter),
        findall(Y, prec(Y, X), PreviousGames),
        length(PreviousGames, CountPrevious),
        CountPrevious > 0,
        Ratio is CountBetter / CountPrevious,
        Ratio >= 0.75.
    
    % normal(X): una partita è normale.
    normal(X) :-
        \+ newbie(X),
        findall(Y, (prec(Y, X), better(X, Y)), BetterGames),
        length(BetterGames, CountBetter),
        findall(Y, prec(Y, X), PreviousGames),
        length(PreviousGames, CountPrevious),
        CountPrevious > 0,
        Ratio is CountBetter / CountPrevious,
        Ratio >= 0.25,
        Ratio < 0.75.
    
    % bad(X): una partita è negativa.
    bad(X) :-
        \+ newbie(X),
        findall(Y, (prec(Y, X), better(X, Y)), BetterGames),
        length(BetterGames, CountBetter),
        findall(Y, prec(Y, X), PreviousGames),
        length(PreviousGames, CountPrevious),
        CountPrevious > 0,
        Ratio is CountBetter / CountPrevious,
        Ratio < 0.25.
    